package ex_210724;

public class DoWhileLoopEX001 {
    public static void main(String[] args) {
        // do while loop
        // do always run before executing while loop
          int i=0;
          do{
              System.out.println(i);
              i++;
          }while (i<=5);    // 1 2 3 4 5


    }
}

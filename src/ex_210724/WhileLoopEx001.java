package ex_210724;

public class WhileLoopEx001 {
    public static void main(String[] args) {
        //   while loop
        // initialization;condition;increment/decrement(updation)
        /* initialization part is outside while loop
       condition and updation  part is inside while loop */

        int i=0;
        while(i<=5){
            System.out.println(i);      // 0 1 2 3 4 5
            i++;
        }

    }
}
